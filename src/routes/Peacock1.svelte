<h2>The peacocks are 35 – 50 in (90 – 130 cm) long.</h2>
<h2>The length of the tail is 1.5 m (5 ft).</h2>
<h2>The peacocks weigh around 4 – 6 kg (8.75 – 13 lbs).</h2>
<h2>The males are relatively more colorful as compared to females.</h2>
<h2>There are more than 200 elongated feathers which the peacocks display.</h2>
<h2>The birds have an insensitive sound.</h2>
<h2>As compared to the elegant beauty of its feathers, peacocks have ugly legs.</h2>
<h2>The females are considerably smaller in size in comparison to the males.</h2>
<h2>One of the fascinating sights to watch is when these birds elaborate a courtship dance through fanning out the tail and preening its feathers.</h2>
<h2>These birds can’t fly like other birds; however, peacocks can take a short flight of few meters above the ground whenever they sense danger.</h2>
<h2>The females are mainly responsible for incubating (keeping warm) eggs and the incubation period lasts for 28 days.</h2>



